    <h1>Hinduisme</h1>

    <div id="contentList">
        <ul>
        <li>Hinduismen er den største religionen i India. Den er over 4500 år gammel.</li>
        <br>
        <li>Hinduismen har blitt til av tradisjoner fra gammelt av, og har ikke bare én grunnlegger.</li>
        <br>
        <li>Hinduer mener at alt som lever, er hellig. De tror også at det finnes mange forskjellige guder.</li>
        <br>
        <li>De tre viktigste gudene er:
            <ul>
                <br>
                <li>Brahma, som skaper verden.</li>
                <li>Vishnu, som opprettholder verden.</li>
                <li>Shiva, som ødelegger verden.</li>
            </ul>
        </li>
        <br>
        <li>Dette viser tre sider ved livet for en hindu. Verden skapes, opprettholdes, ødelegges og skapes igjen.</li>
        <br>
        <li>Slik tror også hinduene at det er for mennesker. Etter at vi dør, gjenfødes vi igjen.</li>
        <br>
        <li>En hindus mål er å bli fri fra gjenfødelsen.</li>
        </ul>
